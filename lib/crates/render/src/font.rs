#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn empty_buffer_is_all_white() {
        let buf = TextBuffer::new();
        let pixels = buf.render_rgba8();
        assert_eq!(pixels.len(), 160 * 144 * 4);
        let white = TextColour::White.to_rgba();
        for y in 0..144 {
            for x in 0..160 {
                let off = (y * 160 + x) * 4;
                assert_eq!(
                    &pixels[off..off + 4],
                    &white,
                    "pixel ({x},{y}) should be white"
                );
            }
        }
    }

    #[test]
    fn single_char_produces_foreground_pixels() {
        let mut buf = TextBuffer::new();
        // Place 'A' (0x41) at col=0,row=0
        buf.set_cell(0, 0, Cell {
            ch: b'A',
            fg: TextColour::Black,
            bg: TextColour::White,
        });
        let pixels = buf.render_rgba8();
        let fg = TextColour::Black.to_rgba();
        let bg = TextColour::White.to_rgba();

        // Check the 8x8 region at (0,0) matches the 'A' glyph
        let glyph = &FONT_8X8[(b'A' - 0x20) as usize];
        let mut found_fg = false;
        for gy in 0..8 {
            for gx in 0..8 {
                let off = (gy * 160 + gx) * 4;
                let on = glyph[gy] & (1 << gx) != 0;
                if on {
                    assert_eq!(&pixels[off..off + 4], &fg);
                    found_fg = true;
                } else {
                    assert_eq!(&pixels[off..off + 4], &bg);
                }
            }
        }
        assert!(found_fg, "'A' glyph should have at least one foreground pixel");
    }

    #[test]
    fn put_str_truncates_at_col_20() {
        let mut buf = TextBuffer::new();
        // Write a 25-char string starting at col 0
        buf.put_str(0, 0, "ABCDEFGHIJKLMNOPQRSTUVWXY", TextColour::DarkGrey, TextColour::White);
        // Only first 20 chars should be placed
        for col in 0..20 {
            assert_eq!(buf.cells[0][col].ch, b'A' + col as u8);
        }
    }

    #[test]
    fn put_str_with_offset_truncates() {
        let mut buf = TextBuffer::new();
        buf.put_str(18, 0, "HELLO", TextColour::DarkGrey, TextColour::White);
        assert_eq!(buf.cells[0][18].ch, b'H');
        assert_eq!(buf.cells[0][19].ch, b'E');
    }

    #[test]
    fn colour_values_match_dmg() {
        assert_eq!(TextColour::White.to_rgba(), [155, 188, 15, 255]);
        assert_eq!(TextColour::LightGrey.to_rgba(), [139, 172, 15, 255]);
        assert_eq!(TextColour::DarkGrey.to_rgba(), [48, 98, 48, 255]);
        assert_eq!(TextColour::Black.to_rgba(), [15, 56, 15, 255]);
    }

    #[test]
    fn out_of_bounds_set_cell_is_no_op() {
        let mut buf = TextBuffer::new();
        buf.set_cell(20, 0, Cell { ch: b'X', fg: TextColour::Black, bg: TextColour::White });
        buf.set_cell(0, 18, Cell { ch: b'X', fg: TextColour::Black, bg: TextColour::White });
        // Should not panic, buffer unchanged
        let pixels = buf.render_rgba8();
        let white = TextColour::White.to_rgba();
        assert_eq!(&pixels[0..4], &white);
    }

    #[test]
    fn wide_text_buffer_correct_size() {
        let buf = WideTextBuffer::new();
        let pixels = buf.render_rgba8();
        assert_eq!(pixels.len(), 640 * 192 * 4);
    }

    #[test]
    fn wide_put_str_truncates_at_80() {
        let mut buf = WideTextBuffer::new();
        let long = "A".repeat(100);
        buf.put_str(0, 0, &long, TextColour::DarkGrey, TextColour::White);
        // Only first 80 chars should be placed
        for col in 0..80 {
            assert_eq!(buf.cells[0][col].ch, b'A');
        }
    }
}
